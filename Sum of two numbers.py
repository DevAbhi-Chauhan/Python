# Input two numbers from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Calculate the sum
sum_result = num1 + num2

# Display the result
print(f"The sum of {num1} and {num2} is {sum_result}")

#In this program:

# 1. We use the input function to get user input for two numbers. float is used to ensure that the input is treated as a floating-point number, allowing for decimal values.

# 2. We calculate the sum of the two numbers and store it in the variable sum_result.

# 3. Finally, we use the print function to display the result, including the numbers entered by the user and their sum.
